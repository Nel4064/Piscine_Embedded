// START and STOP Conditions (p.217)
// The Master initiates and terminates a data transmission. The transmission is initiated when the Master issues a
// START condition on the bus, and it is terminated when the Master issues a STOP condition. Between a START
// and a STOP condition, the bus is considered busy, and no other master should try to seize control of the bus
// START and STOP conditions are signalled by changing the level of the SDA line when the SCL line is high.

// REPEATED START condition (p.217)
// It is used when the Master wishes to initiate a new transfer without relinquishing control of the b

// Address Packet Format (p.217)
// All address packets transmitted on the TWI bus are 9 bits long, consisting of 7 address bits, one READ/WRITE
// control bit and an acknowledge bit. If the READ/WRITE bit is set, a read operation is to be performed, otherwise
// a write operation should be performed.

// Data Packet Format (p.218)
// All data packets transmitted on the TWI bus are nine bits long, consisting of one data byte and an acknowledge
// bit. During a data transfer, the Master generates the clock and the START and STOP conditions, while the
// Receiver is responsible for acknowledging the reception. An Acknowledge (ACK) is signalled by the Receiver
// pulling the SDA line low during the ninth SCL cycle. 

// Why Are Pull-Up Resistors Needed?
// Open-Drain Logic
// I2C devices use open-drain outputs for SDA and SCL.
// This means they can pull the line to GND (LOW) but cannot drive it to VCC (HIGH).
// Without pull-up resistors, the lines would float when no device is pulling them low, leading to undefined logic levels.
// Pull-Up Resistors Provide:
// A default HIGH state for the bus when no device is actively pulling it low.
// A way for devices to share the bus without conflicts (wired-AND logic).
// => If any device pulls the line low, the line is LOW.
// => If no device pulls the line low, the pull-up resistor pulls it HIGH.

